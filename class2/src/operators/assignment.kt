package operators

fun main () {
    var a=11
    var b=5
    //a+=b
    a+=b
    println(a) //a=16...........

    //a-=b
    a-=b
    println(a) //a=11...........

    //a*=b
    a*=b
    println(a) //a=55...........

    //a/=b
    a/=b
    println(a)  //a=11..........

    //a%=b
    a%=b
    println(a)  //a=1............


}